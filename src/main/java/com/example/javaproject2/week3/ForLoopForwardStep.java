package com.example.javaproject2.week3;

public class ForLoopForwardStep {
    public static void main(String[] args) {
        for (int i = 2; i < 10 ; i += 2){
            System.out.println(i);
        }
        for( int j = 1; j < 10 ; j += 2){
            System.out.println(j);
        }
    }
}
